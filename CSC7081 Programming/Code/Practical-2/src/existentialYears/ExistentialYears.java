package existentialYears;

public class ExistentialYears {

	public static void main(String[] args) {
		int yearOfBirth = 1989;
		int yearWhenITurn21 = yearOfBirth + 21;
		int yearWhenITurn40 = yearOfBirth + 40;
		int yearWhenIRetireAt60 = yearOfBirth + 60;
		
		System.out.println("Birth: " + yearOfBirth);
		System.out.println("Turn 21: " + yearWhenITurn21);
		System.out.println("Turn 40: " + yearWhenITurn40);
		System.out.println("Retire: " + yearWhenIRetireAt60);

	}

}
